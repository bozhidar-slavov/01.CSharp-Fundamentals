using System;

class FloatOrDouble
{
    static void Main()
    {
        double firstDoubleNumber = 34.567839023;
        float firstFloatNumber = 12.345F;
        double secondDoubleNumber = 8923.1234857;
        float secondFloatNumber = 3456.091F;

        Console.WriteLine("{0}\n{1}\n{2}\n{3}", firstDoubleNumber, firstFloatNumber, secondDoubleNumber, secondFloatNumber);
    }
}